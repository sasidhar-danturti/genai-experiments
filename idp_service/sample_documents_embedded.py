def has_embedded(name: str) -> bool:
    return name in _EMBEDDED_DOCUMENTS

def write_embedded(name: str, target: Path) -> bool:
    payload = _EMBEDDED_DOCUMENTS.get(name)
    if not payload:
        return False
    target.write_bytes(base64.b64decode(payload.encode('ascii')))
    return True

